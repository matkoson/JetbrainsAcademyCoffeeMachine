enum class DaysOfTheWeek {
    MONDAY, TUESDAY, WEDNESDAY, THURSDAY, FRIDAY, SATURDAY, SUNDAY
}

fun main() {
    println(DaysOfTheWeek.SUNDAY)
    println(DaysOfTheWeek.MONDAY)
    println(DaysOfTheWeek.TUESDAY)
    println(DaysOfTheWeek.WEDNESDAY)
    println(DaysOfTheWeek.THURSDAY)
    println(DaysOfTheWeek.FRIDAY)
    println(DaysOfTheWeek.SATURDAY)
}
